export function somar(num1: number, num2: number): number {
  return num1 + num2;
}

export function subtracao(num1: number, num2: number): number {
  return num1 - num2;
}

console.log("Função somar executada. Resultado: " + somar(1, 3));
console.log("Função subtracao executada. Resultado: " + subtracao(3, 1));
